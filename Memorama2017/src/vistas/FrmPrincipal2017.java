    /**
     * verifica si se ha iniciado el juego, ademas del tipo 
     * y del numero de tarjetas seleccionadas.
     * segun el metodo verificar  colorea la tarjeta del color 
     * que le corresponde al jugador que acerto, en caso contrario
     * vuelve todo a su normalidad.
     * ademas indica que jugador ha ganado
     */
    private void revisar() {

        if (m1.getnSel() == 2 && m1.isBanTiro1() && banJuego) {
            if (!m1.verificar()) {
                tablero[m1.getTiro1()].setIcon(new ImageIcon(getClass().
                        getResource(fondo)));
                tablero[m1.getTiro2()].setIcon(new ImageIcon(getClass().
                        getResource(fondo)));

                tarjetas[m1.getTiro1()].setBackground(Color.white);
                tarjetas[m1.getTiro2()].setBackground(Color.white);

                explosion();
                m1.setnSel(0);

            } else {
                numGanador++;
                if (m1.isTurnoJ1()) {
                    tarjetas[m1.getTiro1()].setBackground(Color.RED);
                    tarjetas[m1.getTiro2()].setBackground(Color.RED);
                } else {
                    tarjetas[m1.getTiro1()].setBackground(Color.blue);
                    tarjetas[m1.getTiro2()].setBackground(Color.blue);
                }
                exclamacion();
                m1.setnSel(0);
                lblPuntosJ1.setText("" + m1.getParesJ1());
                lblPuntosJ2.setText("" + m1.getParesJ2());

            }
        }
        if (m1.isTurnoJ1()) {
            lblJugador1.setBorder(javax.swing.BorderFactory.createLineBorder(Color.yellow));
            lblJugador1.setForeground(Color.yellow);
            lblJugador2.setBorder(javax.swing.BorderFactory.createLineBorder(null));
            lblJugador2.setForeground(Color.black);

        } else {

            lblJugador2.setBorder(javax.swing.BorderFactory.createLineBorder(Color.yellow));
            lblJugador2.setForeground(Color.yellow);
            lblJugador1.setBorder(javax.swing.BorderFactory.createLineBorder(null));
            lblJugador1.setForeground(Color.black);
        }
        if (numGanador == 12) {
            if (m1.getParesJ1() > m1.getParesJ2()) {
                JOptionPane.showMessageDialog(this, "GANO EL JUGADOR :" + lblJugador1.getText());
                numGanador=0;
            } else if (m1.getParesJ2() > m1.getParesJ1()){
                JOptionPane.showMessageDialog(this, "GANO EL JUGADOR :" + lblJugador2.getText());
                numGanador=0;
            }else {
            JOptionPane.showMessageDialog(this, "Empate");
            }
        }

    }

