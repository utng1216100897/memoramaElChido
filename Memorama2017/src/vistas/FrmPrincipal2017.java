/**
 * si el juego a iniciado, selecciona las tarjetas 
 * @param pos 
 */
    private void seleccionar(int pos) {

        if (banJuego) {
            if (m1.setTiro(pos)) { // revisa si el tiro seleccionado es valido
                tarjetas[pos].setBackground(Color.YELLOW);
//                JOptionPane.showMessageDialog(this, m1.getCategoria() + m1.getTablero(pos));
                tablero[pos].setIcon(new ImageIcon(getClass().getResource(m1.getCategoria() + m1.getTablero(pos))));

                m1.setTiro(pos);
            }
        }

    }

